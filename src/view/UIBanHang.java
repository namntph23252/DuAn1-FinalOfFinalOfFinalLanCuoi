    private void loadTableGioHang(List<SanPhamViewModels> list) {

        DefaultTableModel defaultTableModel = (DefaultTableModel) tb_gioHang.getModel();
        defaultTableModel.setRowCount(0);
        for (int i = 0; i < list.size(); i++) {
            defaultTableModel.addRow(new Object[]{
                list.get(i).getId(), list.get(i).getMa(), list.get(i).getTen(), list.get(i).getSoLuong(), list.get(i).getGiaBan(),
                list.get(i).getSoLuong() * list.get(i).getGiaBan()
            });
        }
        DefaultTableModel defaultTableModelGioHang = (DefaultTableModel) tb_gioHang.getModel();
        double tongTien = tinhTongThanhTien(defaultTableModelGioHang);
        txt_tongTien.setText(Double.toString(tongTien));
        txt_khachCanTra.setText(Double.toString(tongTien));
    }

    public double tinhTongThanhTien(DefaultTableModel model) {
        double tongThanhTien = 0.0;

        for (int i = 0; i < model.getRowCount(); i++) {
            double thanhTien = (double) model.getValueAt(i, 5); // Lấy giá trị của cột thành tiền (chỉ số 4)
            tongThanhTien += thanhTien;
        }
        return tongThanhTien;
    }
