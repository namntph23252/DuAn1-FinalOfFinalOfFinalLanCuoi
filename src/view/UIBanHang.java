    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int row = tb_gioHang.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Hay chons an pham de cap nhat so luong!");
            return;
        }
        String id = tb_gioHang.getValueAt(row, 0).toString();
        String soLuongSuaStr = txt_soLuongSua.getText();
        if (isPositiveInteger(soLuongSuaStr) == false) {
            JOptionPane.showMessageDialog(this, "Ban nhap sai so luong!");
            return;
        }
        int soLuongSua = Integer.parseInt(soLuongSuaStr);
        for (SanPhamViewModels sanPhamViewModels : listGioHang) {
            if (soLuongSua > 0) {
                if (sanPhamViewModels.getId().equalsIgnoreCase(id)) {
                    sanPhamViewModels.setSoLuong(soLuongSua);
                    break;
                }
            } else if (soLuongSua == 0) {
                if (sanPhamViewModels.getId().equalsIgnoreCase(id)) {
                    listGioHang.remove(sanPhamViewModels);
                    break;
                }
            }

        }
        loadTableGioHang(listGioHang);


    }//GEN-LAST:event_jButton3ActionPerformed
    public static boolean isPositiveInteger(String input) {
        try {
            // Chuyển chuỗi đầu vào thành số nguyên
            int num = Integer.parseInt(input);
            // Kiểm tra số nguyên có lớn hơn 0 hay không
            if (num >= 0) {
                return true; // Là số nguyên dương
            } else {
                return false; // Không là số nguyên dương
            }
        } catch (NumberFormatException e) {
            // Nếu không thể chuyển đổi chuỗi thành số nguyên, đây không phải là số nguyên dương
            return false;
        }
    }
    private void tb_listSachMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_listSachMouseClicked
        // TODO add your handling code here:

        int row = tb_listSach.getSelectedRow();
        String id = tb_listSach.getValueAt(row, 1).toString();
        String ma = tb_listSach.getValueAt(row, 2).toString();
        String ten = tb_listSach.getValueAt(row, 3).toString();
        int soLuong = 1;

        double giaBan = Double.parseDouble(tb_listSach.getValueAt(row, 6).toString());
        SanPhamViewModels sach = new SanPhamViewModels(id, ma, ten, giaBan, soLuong);
        boolean daTangSoLuong = false;
        for (SanPhamViewModels s : listGioHang) {
            if (s.getId().equalsIgnoreCase(sach.getId())) {
                s.setSoLuong(s.getSoLuong() + 1);
                daTangSoLuong = true;
                break;
            }
        }

        if (!daTangSoLuong) {
            sach.setSoLuong(1);
            listGioHang.add(sach);
        }

        loadTableGioHang(listGioHang);
        // sau khi load len giohang, ta se tinh tien


    }//GEN-LAST:event_tb_listSachMouseClicked

    private void btn_thanhToanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_thanhToanActionPerformed
        // TODO add your handling code here:
//         them hoa don
        if (tb_gioHang.getRowCount() <= 0) {
            JOptionPane.showMessageDialog(this, "Hãy chọn sách!");
            return;
        }
        int xacNhanThnahToan = JOptionPane.showConfirmDialog(this, "Xác nhận thanh toán!", "thanh toAN", JOptionPane.YES_NO_OPTION);
        if (xacNhanThnahToan != JOptionPane.YES_OPTION) {
            return;
        }
        HoaDon hoadon = this.getHoaDon();
        int idhoaDon = this.hoaDonService.insert(hoadon);
//         lấy ra mã hóa đơn để in hóa đơn
        HoaDonfinalViewModels hdon = this.hoaDonService.getonewithid(idhoaDon);
        String maHD = hdon.getMa();

// them vao hoa don chi tiet
        for (int i = 0; i < tb_gioHang.getRowCount(); i++) {
            String idSach = tb_gioHang.getValueAt(i, 0).toString();
            int soluong = Integer.parseInt(tb_gioHang.getValueAt(i, 3).toString());
            double donGia = Double.parseDouble(tb_gioHang.getValueAt(i, 4).toString());
            double thanhtien = Double.parseDouble(tb_gioHang.getValueAt(i, 5).toString());
            HoaDonChiTiet hoaDonChiTiet = new HoaDonChiTiet(idSach, donGia, soluong, thanhtien);
            this.hoaDonChiTietService.insert(hoaDonChiTiet, String.valueOf(idhoaDon));
        }
        int traLoi = JOptionPane.showConfirmDialog(this, "Bạn có muốn in hóa đơn không", "In Hóa Đơn", JOptionPane.YES_NO_OPTION);
        if (traLoi == JOptionPane.YES_OPTION) {
            // Tạo một đối tượng JDialog
            JDialog dialog = new JDialog();
            dialog.setTitle("Thông tin thanh toán");
            dialog.setSize(400, 300);

            // Tạo một JTextArea
            JTextArea b = new JTextArea();
            b.setEditable(false); // Có thể chỉnh sửa nội dung trong JTextArea hay không
            dialog.add(b, BorderLayout.CENTER); // Đưa JTextArea vào vị trí trung tâm của JDialog

            // them noi dung bill vao day
            b.setText(b.getText() + "                                        Cửa Hàng Sach \n");
            b.setText(b.getText() + "-------------------------------------------------------------------------------\n");
            b.setText(b.getText() + "                                        Mã Hóa Đơn " + maHD + "\n");
            b.setText(b.getText() + "-------------------------------------------------------------------------------\n");
            b.setText(b.getText() + "  Sản Phẩm \tSố Lượng \tGiá \tThành Tiền" + "\n");
            b.setText(b.getText() + "--------------------------------------------------------------------------------\n");

            DefaultTableModel df = (DefaultTableModel) tb_gioHang.getModel();

            // Lấy thông tin sản phẩm từ bảng
            for (int i = 0; i < tb_gioHang.getRowCount(); i++) {
                String Name = df.getValueAt(i, 2).toString();
                String Qty = df.getValueAt(i, 3).toString();
                String Price = df.getValueAt(i, 4).toString();
                String Total = df.getValueAt(i, 5).toString();

                // Kiểm tra độ dài của tên sản phẩm
                if (Name.length() > 10) {
                    Name = Name.substring(0, 10) + "..."; // Giới hạn tên sản phẩm chỉ hiển thị 10 ký tự
                }

                b.setText(b.getText() + "  " + Name + "\t" + Qty + "\t" + Price + "\t" + Total + "\n");
            }

            b.setText(b.getText() + "--------------------------------------------------------------------------------\n");
            b.setText(b.getText() + "Tổng Tiền : \t\t\t" + txt_tongTien.getText() + "\n");
            b.setText(b.getText() + "--------------------------------------------------------------------------------\n");
            b.setText(b.getText() + "Cảm ơn quý khách!" + "\n");

            // Tạo một JButton
            JButton btnIn = new JButton("In Hóa Đơn");
            btnIn.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    try {
                        b.print();
                    } catch (PrinterException ex) {
                        Logger.getLogger(UIBanHang.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            });
            dialog.add(btnIn, BorderLayout.SOUTH); // Đưa JButton vào vị trí dưới cùng của JDialog

            // Hiển thị JDialog
            dialog.setVisible(true);
        }

        listGioHang.clear();
        loadTableGioHang(listGioHang);

    }//GEN-LAST:event_btn_thanhToanActionPerformed

