    private String getDate() {
        // lay ra ngay hien tai
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd HH:mm:ss");
        LocalDateTime now = LocalDateTime.now();
        return dtf.format(now);
    }

    private void loadTableHoaDon(List<HoaDonfinalViewModels> list) {
        DefaultTableModel tbHHoaDonModel = (DefaultTableModel) tb_hoaDon.getModel();
        tbHHoaDonModel.setRowCount(0);
        for (HoaDonfinalViewModels hoaDonViewModels : list) {
            tbHHoaDonModel.addRow(new Object[]{
                hoaDonViewModels.getId(),
                hoaDonViewModels.getMa(),
                hoaDonViewModels.getTongTien(),
                0,
                hoaDonViewModels.getKhachCanTra(),
                hoaDonViewModels.getNgayTao(),
                hoaDonViewModels.getTenNguoiDung()
            });
        }
    }

    private void loadHoadonChiTiet(List<HoaDonChiTietViewModels> list) {
        DefaultTableModel tbHHoaDonModel = (DefaultTableModel) tb_hoaDonCt.getModel();
        tbHHoaDonModel.setRowCount(0);
        for (HoaDonChiTietViewModels hoaDonctViewModels : list) {
            tbHHoaDonModel.addRow(new Object[]{
                hoaDonctViewModels.getMa(),
                hoaDonctViewModels.getTenSach(),
                hoaDonctViewModels.getSoLuong(),
                hoaDonctViewModels.getDonGia(),
                hoaDonctViewModels.getThanhTien(),});
        }
    }

