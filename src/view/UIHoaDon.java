    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        int traLoi = JOptionPane.showConfirmDialog(this, "Bạn có muốn in hóa đơn không", "In Hóa Đơn", JOptionPane.YES_NO_OPTION);
        int rowHD = tb_hoaDon.getSelectedRow();
        String maHD = tb_hoaDon.getValueAt(rowHD, 1).toString();
        if (traLoi == JOptionPane.YES_OPTION) {
            // Tạo một đối tượng JDialog
            JDialog dialog = new JDialog();
            dialog.setTitle("Thông tin thanh toán");
            dialog.setSize(400, 300);

            // Tạo một JTextArea
            JTextArea b = new JTextArea();
            b.setEditable(false); // Có thể chỉnh sửa nội dung trong JTextArea hay không
            dialog.add(b, BorderLayout.CENTER); // Đưa JTextArea vào vị trí trung tâm của JDialog

            // them noi dung bill vao day
            b.setText(b.getText() + "                                        Cửa Hàng Sach \n");
            b.setText(b.getText() + "-------------------------------------------------------------------------------\n");
            b.setText(b.getText() + "                                        Mã Hóa Đơn " + maHD + "\n");
            b.setText(b.getText() + "-------------------------------------------------------------------------------\n");
            b.setText(b.getText() + "  Sản Phẩm \tSố Lượng \tGiá \tThành Tiền" + "\n");
            b.setText(b.getText() + "--------------------------------------------------------------------------------\n");

            DefaultTableModel df = (DefaultTableModel) tb_hoaDonCt.getModel();

            // Lấy thông tin sản phẩm từ bảng
            for (int i = 0; i < tb_hoaDonCt.getRowCount(); i++) {
                String Name = df.getValueAt(i, 1).toString();
                String Qty = df.getValueAt(i, 2).toString();
                String Price = df.getValueAt(i, 3).toString();
                String Total = df.getValueAt(i, 4).toString();

                // Kiểm tra độ dài của tên sản phẩm
                if (Name.length() > 10) {
                    Name = Name.substring(0, 10) + "..."; // Giới hạn tên sản phẩm chỉ hiển thị 10 ký tự
                }

                b.setText(b.getText() + "  " + Name + "\t" + Qty + "\t" + Price + "\t" + Total + "\n");
            }

            b.setText(b.getText() + "--------------------------------------------------------------------------------\n");
            b.setText(b.getText() + "Tổng Tiền : \t\t\t" + tb_hoaDon.getValueAt(rowHD, 2) + "\n");
            b.setText(b.getText() + "--------------------------------------------------------------------------------\n");
            b.setText(b.getText() + "Cảm ơn quý khách!" + "\n");

            // Tạo một JButton
            JButton btnIn = new JButton("In Hóa Đơn");
            btnIn.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    try {
                        b.print();
                    } catch (PrinterException ex) {
                        Logger.getLogger(UIBanHang.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            });
            dialog.add(btnIn, BorderLayout.SOUTH); // Đưa JButton vào vị trí dưới cùng của JDialog

            // Hiển thị JDialog
            dialog.setVisible(true);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void btnXuatfileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXuatfileActionPerformed
        // TODO add your handling code here:
        try {
            XSSFWorkbook worbook = new XSSFWorkbook();
            XSSFSheet sheet = worbook.createSheet("Hoa Don");

            XSSFRow row = null;
            Cell cell = null;

            row = sheet.createRow(3);
            cell = row.createCell(0, CellType.NUMERIC);
            cell.setCellValue("STT");

            cell = row.createCell(1, CellType.STRING);
            cell.setCellValue("ID");

            cell = row.createCell(2, CellType.STRING);
            cell.setCellValue("Mã");

            cell = row.createCell(3, CellType.STRING);
            cell.setCellValue("Tổng Tiền Hàng");

            cell = row.createCell(4, CellType.STRING);
            cell.setCellValue("Giam Giá");
            
            cell = row.createCell(5, CellType.STRING);
            cell.setCellValue("Khách Ðã Tra");
            
            cell = row.createCell(6, CellType.STRING);
            cell.setCellValue("Thoi Gian");
            
            cell = row.createCell(7, CellType.STRING);
            cell.setCellValue("Nguoi Nhân Ðon");
            
            List<HoaDonfinalViewModels> list = hoaDonService.getAll();

            if (list != null) {
                int s = list.size();
                for (int i = 0; i < s; i++) {
                    HoaDonfinalViewModels hd = list.get(i);
                    row = sheet.createRow(4 + i);

                    cell = row.createCell(0, CellType.NUMERIC);
                    cell.setCellValue(i + 1);
                    cell = row.createCell(01, CellType.STRING);
                    cell.setCellValue(hd.getId());
                    
                    cell = row.createCell(02, CellType.STRING);
                    cell.setCellValue(hd.getMa());
                    
                    cell = row.createCell(03, CellType.STRING);
                    cell.setCellValue(hd.getTongTien());
                    
                    cell = row.createCell(04, CellType.STRING);
                    cell.setCellValue(0);

                    cell = row.createCell(06, CellType.STRING);
                    cell.setCellValue(hd.getNgayTao().toString());

                    cell = row.createCell(05, CellType.STRING);
                    cell.setCellValue(hd.getKhachCanTra());

                    cell = row.createCell(07, CellType.STRING);
                    cell.setCellValue(hd.getTenNguoiDung());

                }
                File f = new File("E:\\FileThongKe.xlsx");
                try {
                    FileOutputStream fis = new FileOutputStream(f);

                    worbook.write(fis);
                    fis.close();
                } catch (FileNotFoundException e) {
                    e.printStackTrace();
                }
            }
        } catch (Exception ex) {
            ex.printStackTrace();

        }
        JOptionPane.showMessageDialog(this, "Xuất Thành Công");                        

    }//GEN-LAST:event_btnXuatfileActionPerformed

