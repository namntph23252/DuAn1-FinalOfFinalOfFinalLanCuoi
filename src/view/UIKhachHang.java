    private boolean validatePhoneNumber(String phoneNumber) {
        String regex = "^0[1-9]{9}$";
        return phoneNumber.matches(regex);
    }

//    kiem tra dinh dang email
    public static boolean isValidEmail(String email) {
        String regex = "^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{2,}$";
        return email.matches(regex);
    }

    public KhachHang getData() {
        String ten = txtTen.getText().trim();
        if (ten.equals("")) {
            JOptionPane.showMessageDialog(this, "Nhap ten!");
            txtTen.requestFocus();
            return null;
        }
        String soDt = txtSoDt.getText().trim();
        if (soDt.equals("") && soDt.isBlank() == false) {
            JOptionPane.showMessageDialog(this, "Nhap so dien thoai!");
            txtSoDt.requestFocus();
            return null;
        }
//        else if (this.validatePhoneNumber(soDt) == false &&soDt.isBlank() == false) {
//            JOptionPane.showMessageDialog(this, "So dien thoai khong dung dinh dang!");
//            txtSoDt.requestFocus();
//            return null;
//        }
        String email = txtEmail.getText().trim();
        if (email.equals("") && email.isBlank() == false) {
            JOptionPane.showMessageDialog(this, "Nhap so email!");
            txtEmail.requestFocus();
            return null;
        }
        if (isValidEmail(email) == false && email.isBlank() == false) {
            JOptionPane.showMessageDialog(this, "Email khong dung dinh dang!");
            txtEmail.requestFocus();
            return null;
        }

        if (rdio_Dang_HD.isSelected() == false && rdio_Ngung_HD.isSelected() == false) {
            JOptionPane.showMessageDialog(this, "Chon trang thai");
            return null;
        }
        int trangThai = 0;
        if (rdio_Dang_HD.isSelected()) {
            trangThai = 0;
        } else {
            trangThai = 1;
        }

        KhachHang khachHang = new KhachHang(ten, soDt, email, trangThai);
        return khachHang;
    }

    public void LoadToTable(List<KhachHang> listKH) {
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0);
        for (KhachHang khachHang : listKH) {
            model.addRow(new Object[]{
                khachHang.getId(), khachHang.getMa(), khachHang.getTen(), khachHang.getSoDt(), khachHang.getEmail(), checkTT(khachHang.getTrangThai())
            });
        }
    }

