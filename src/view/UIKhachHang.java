        txtMa.setText(table.getValueAt(row, 1).toString());
        txtTen.setText(table.getValueAt(row, 2).toString());
        txtSoDt.setText(table.getValueAt(row, 3).toString());
        txtEmail.setText(table.getValueAt(row, 4).toString());
