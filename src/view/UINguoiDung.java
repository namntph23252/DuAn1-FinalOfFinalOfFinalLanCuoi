    private NguoiDung getData() {
        String ten = txt_ten.getText().trim();
        if (ten.equals("")) {
            JOptionPane.showMessageDialog(this, "Khong de trong ten");
            return null;
        }
        String sdt = txt_Sdt.getText().trim();
        if (this.validatePhoneNumber(sdt) == false) {
            JOptionPane.showMessageDialog(this, "SDt khong hop le");
            return null;
        }
        String matKhau = txt_matKhau.getText().trim();
        if (matKhau.equals("")) {
            JOptionPane.showMessageDialog(this, "Khong de trong matkhau");
            return null;
        }
        String diaChi = txt_diaChi.getText().trim();
        if (diaChi.equals("")) {
            JOptionPane.showMessageDialog(this, "Khong de trong ten");
            return null;
        }
        String gioiTinhStr = cbb_gioiTinh.getSelectedItem().toString();
        int gioiTinh = 0;
        if (gioiTinhStr.equalsIgnoreCase("Nam")) {
            gioiTinh = 0;
        } else {
            gioiTinh = 1;
        }
        String chucVu = cbb_chucVu.getSelectedItem().toString();
        if (chucVu.equals("")) {
            JOptionPane.showMessageDialog(this, "Phải chọn chức vụ!");
            return null;
        }
        int trangThai = 0;
        String trangThaiStr = cbb_trangThai.getSelectedItem().toString();
        if (trangThaiStr.equalsIgnoreCase("Đang làm việc")) {
            trangThai = 0;
        } else {
            trangThai = 1;
        }
        Date date = jdate_ngSinh.getDate();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy/MM/dd");
        String formattedDate = sdf.format(date);
        String ngaySinh = formattedDate.toString();
        String emial = txt_email1.getText().trim();
        if (emial.equals("") == false) {
            if (isValidEmail(emial) == false) {
                JOptionPane.showMessageDialog(this, "Email khong dung dinh dang!!");
                return null;
            }
        }
        String anh = null;
        if (duongdananh == null) {
            anh = null;
        } else {
            anh = duongdananh;
        }

        NguoiDung ng = new NguoiDung(ten, emial, gioiTinh, ngaySinh, diaChi, sdt, matKhau, chucVu, anh, trangThai);
        return ng;
    }

    public void LoadTableAll(List<NguoiDung> list) {
        DefaultTableModel defaultTableModel = (DefaultTableModel) tb_nhanVien.getModel();
        defaultTableModel.setRowCount(0);
        for (int i = 0; i < list.size(); i++) {
            defaultTableModel.addRow(new Object[]{
                i + 1,
                list.get(i).getMa(),
                list.get(i).getHoTen(),
                list.get(i).getSdt(),
                list.get(i).getMatKhau(),
                list.get(i).getDiaChi(),
                getGioiTinh(list.get(i).getGioiTinh()),
                list.get(i).getChucVu(),
                getTrangThai(list.get(i).getTrangThai()),
                list.get(i).getNgaySinh(),
                list.get(i).getEmail(),
                list.get(i).getImage()
            });
        }
    }
