    private String getGioiTinh(int gioiTinh) {
        String gioiTinhReturn;
        if (gioiTinh == 0) {
            gioiTinhReturn = "Nam";
        } else {
            gioiTinhReturn = "Nữ";
        }
        return gioiTinhReturn;
    }
