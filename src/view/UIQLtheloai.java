    private void btn_timkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_timkiemActionPerformed
        // TODO add your handling code here:
        String ten = txtTenTim.getText().trim();
        if (ten.equals("")) {
            JOptionPane.showMessageDialog(this, "Hay Nhap Ten Can Tim!");
            txtTenTim.requestFocus();
            return;
        } else {
            List<TheLoai> listSearch = this.svtheloai.timKiem(ten);
            DefaultTableModel defaultTableModel = (DefaultTableModel) tb_theLoai.getModel();
            defaultTableModel.setRowCount(0);
            for (int i = 0; i < svtheloai.timKiem(ten).size(); i++) {
                defaultTableModel.addRow(new Object[]{
                    i + 1,
                    svtheloai.timKiem(ten).get(i).getId(),
                    svtheloai.timKiem(ten).get(i).getMa(),
                    svtheloai.timKiem(ten).get(i).getTen(),
                    getTrangThai(svtheloai.timKiem(ten).get(i).getTrangthai())
                });
            }
        }
    }//GEN-LAST:event_btn_timkiemActionPerformed

