    private void tb_theLoaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_theLoaiMouseClicked
        // TODO add your handling code here:
        int row = tb_theLoai.getSelectedRow();
        txt_idTheloai.setText((String) tb_theLoai.getValueAt(row, 0));
        txt_maTheLoai.setText((String) tb_theLoai.getValueAt(row, 1));
        txt_tenTheLoai.setText((String) tb_theLoai.getValueAt(row, 2));
        
        if (tb_theLoai.getValueAt(row, 3).toString().equalsIgnoreCase("Ðang kinh doanh")) {
            rd_dangkinhdoanh.setSelected(true);
        } else {
            rd_ngungkinhdaonh.setSelected(true);
        }
    }//GEN-LAST:event_tb_theLoaiMouseClicked

