    public void LoadTable(List<TheLoai> listTl) {
        DefaultTableModel model = (DefaultTableModel) tb_theLoai.getModel();
        model.setRowCount(0);
        for (TheLoai theLoai : listTl) {
            model.addRow(new Object[]{
                theLoai.getId(), theLoai.getMa(), theLoai.getTen(), getTrangThai(theLoai.getTrangthai())
            });
        }
    }
    
    public String getdate() {
        // lay ra ngay hien tai
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd HH:mm:ss");
        LocalDateTime now = LocalDateTime.now();
        return dtf.format(now);
    }
    
