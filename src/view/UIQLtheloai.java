    public String getTrangThai(int TrangThai) {
        String trangThai;
        if (TrangThai == 0) {
            trangThai = "Ðang kinh doanh";
        } else {
            trangThai = "Ngừng kinh doanh";
        }
        return trangThai;
    }
    
    public TheLoai getData() {
        String id = txt_idTheloai.getText().trim();
        String ma = txt_maTheLoai.getText().trim();
        String ten = txt_tenTheLoai.getText().trim();
        String ngayTao = this.getdate();
        String ngaySua = this.getdate();
        int trangThai = 0;
        if (rd_dangkinhdoanh.isSelected()) {
            trangThai = 0;
        } else {
            trangThai = 1;
        }
        if (ten.length() == 0 || ten.length() == 0) {
            JOptionPane.showMessageDialog(this, "Khong bo trong thon tin!");
            return null;
        }
        TheLoai s = new TheLoai(id, ma, ten, ngayTao, ngaySua, trangThai);
        return s;
    }
    
    public String checkTT(int tt) {
        if (tt == 1) {
            return "Ðang kinh doanh";
        } else {
            return "Ngừng kinh doanh";
        }
    }

