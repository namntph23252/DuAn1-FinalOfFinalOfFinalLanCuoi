    private void tbl_TacGiaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_TacGiaMouseClicked
        // TODO add your handling code here:
        int row = tbl_TacGia.getSelectedRow();
        txtid.setText(tbl_TacGia.getValueAt(row, 0).toString());
        txtma.setText(tbl_TacGia.getValueAt(row, 1).toString());
        txtten.setText(tbl_TacGia.getValueAt(row, 2).toString());
        if (tbl_TacGia.getValueAt(row, 3).toString().equals("Ðang kinh doanh")) {
            rdio_dang_kd.setSelected(true);
            rdo_ngung_kd.setSelected(false);
        } else {
            rdio_dang_kd.setSelected(false);
            rdo_ngung_kd.setSelected(true);
        }
    }//GEN-LAST:event_tbl_TacGiaMouseClicked

    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed

        tacGia tg = this.getData();
        if (tg == null) {
            return;
        }
        JOptionPane.showMessageDialog(this, this.tGService.add(tg));
        LoadToTable(tGService.getAll());
    }//GEN-LAST:event_btnthemActionPerformed
    public tacGia getData() {
        String ten = txtten.getText().trim();
        if (ten.equals("")) {
            JOptionPane.showMessageDialog(this, "Nhap ten!");
            txtten.requestFocus();// tra vee nhap
            return null;
        }
        int trangThai = 0;

        if (rdio_dang_kd.isSelected()) {
            trangThai = 0;
        } else {
            trangThai = 1;
        }
        tacGia tg = new tacGia(ten, trangThai);
        return tg;
    }
    private void btncapnhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncapnhatActionPerformed

        String Id = txtid.getText();
        tacGia Tg = this.getData();
        if (Tg == null) {
            return;
        }
        JOptionPane.showMessageDialog(this, this.tGService.update(Tg, Id));
        LoadToTable(tGService.getAll());
    }//GEN-LAST:event_btncapnhatActionPerformed

    private void btntimkiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntimkiemActionPerformed
        // TODO add your handling code here:
        String ten = txtTenTim.getText().trim();
        if (ten.equals("")) {
            JOptionPane.showMessageDialog(this, "Hay Nhap Ten Can Tim!");
            txtTenTim.requestFocus();
            return;
        } else {
            List<tacGia> listSearch = this.tGService.timKiem(ten);
            LoadToTable(listSearch);
        }
    }//GEN-LAST:event_btntimkiemActionPerformed

