    /**
     * @param listTG
     * @param args the command line arguments
     */
    public String checkTT(int tt) {
        if (tt == 0) {
            return "Ðang kinh doanh";
        } else {
            return "Ngừng kinh doanh";
        }
    }

    public void LoadToTable(List<tacGia> listTG) {
        DefaultTableModel model = (DefaultTableModel) tbl_TacGia.getModel();
        model.setRowCount(0);
        for (tacGia tg : listTG) {
            model.addRow(new Object[]{
                tg.getId(), tg.getMa(), tg.getTen(), checkTT(tg.getTrangThai())

            });
        }
    }

